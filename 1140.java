import java.util.Scanner;
 
public class Main
{
    public static void main(String[] args)
    {
        Scanner in = new Scanner(System.in);
        int sum = 0, m;
        int n = in.nextInt ();
        do {
            sum += n;
            m = n;
            n = in.nextInt ();
        }
        while (n > 0 || m > 0);
        System.out.println (sum);
    }
}
